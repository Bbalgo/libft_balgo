#include	"libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int	i;
	char			*p;

	i = 0;
	p = b;
	while (i < len)
	{
		p[i] = c;
		i++;
	}
	return (b);
}
